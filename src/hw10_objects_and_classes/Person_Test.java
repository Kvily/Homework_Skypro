package hw10_objects_and_classes;

public class Person_Test {
    private String name;
    private int age;

    public Person_Test(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public int getAge() {
        return this.age;
    }
}
